/*
 Create an array of your favourite animals.
 Print out how many animals you have in your array.
 Print out the second element in that array.
 */
func favouriteAnimals() {
    let animals: Array<String> = ["dog", "tiger", "eagle"]
    print("I have \(animals.count) animals")
    print("The second animal is \(animals[1])")
}
